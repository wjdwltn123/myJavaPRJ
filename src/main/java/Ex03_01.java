public class Ex03_01 {
    public static void main(String[] args){
        System.out.printf("100+100");       // 문자열로 나타기때문에 100+100으로 출력이된다.

        System.out.printf("\n");        // 다음행으로 넘어간다

        System.out.printf("%d", 100 + 100); // %d는 정수를 나타내는 형태이므로 200이 된다

        System.out.printf("\n"); // 다음행으로 넘어간다

    }

}
